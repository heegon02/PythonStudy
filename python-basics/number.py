###### 숫자 연산자
print(15, "+", 4, "=", 15+4) #덧셈 연산자
print(15, "-", 4, "=", 15-4) #뺄셈 연산자
print(15, "*", 4, "=", 15*4) #곱셈 연산자
print(15, "/", 4, "=", 15/4) #나눗셈 연산자
print(16, "/", 4, "=", 16/4) #는 다음과 같이 정수 결과여도, 실수 형태로 기본 출력함.
print(15, "//", 4, "=", 15//4) #몫 연산자
print(15, "%", 4, "=", 15%4) #나머지 연산자