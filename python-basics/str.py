###### 문자열 관련 함수
a = " HapPy BiRthdAy "
print(a) #단순 출력
print(type(a)) #자료형 출력
print(a.upper()) #대문자로 변환
print(a.lower()) #소문자로 변환
print(a.swapcase()) #대소문자를 서로 변환
print(a.split(" ")) #공백을 기준으로 문자열 나눔
print(a.strip()) #문자열의 양쪽 여백 제거
print(a.strip()[:3]) #[:]를 이용하여, 문자열 인덱싱 혹은 슬라이싱
print("안녕하세요"[:3]) #문자열 인덱싱 출력